# use for else loop
print(">>> 1 To 6 Without Break <<<");
for i in range(6) :
    print("> ",i);
else : 
    print(">>> Finish Successfully <<<");

# use for else loop with break
print("\n\n>>> 10 To 15 With Break <<<");
for i in range(10,15) :
    print("> ",i);
    break;
else :
    print(">>> Finish Successfully <<<");